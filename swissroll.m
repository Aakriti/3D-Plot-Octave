N = 1000;

r=linspace(0,1,N);

t = (3*pi/2)*(0.5+1.7*r);
x1 = t.*cos(t);
y1 = t.*sin(t);
z1 = 35*rand(1,N);

figure(1);
plot3(x1(1:250), y1(1:250), z1(1:250),'+r',x1(251:500), y1(251:500), z1(251:500),'+c',x1(501:750), y1(501:750), z1(501:750),'+g',x1(751:1000), y1(751:1000), z1(751:1000),'+b');

D = [x1; y1; z1];
dlmwrite('swiss_roll.txt',D',"delimiter","  ");

pause

